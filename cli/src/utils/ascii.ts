export function pinkBox(title: string, lines: string[]) {
  const width = Math.max(title.length, ...lines.map(l => l.length)) + 4;
  const top = PINK('‚ĒĆ' + '‚ĒÄ'.repeat(width - 2) + '‚Ēź');
  const bottom = PINK('‚ĒĒ' + '‚ĒÄ'.repeat(width - 2) + '‚Ēė');
  const titleLine = PINK('‚Ēā ' + BOLD_PINK(title.padEnd(width - 4)) + ' ‚Ēā');
  const contentLines = lines.map(l => PINK('‚Ēā ' + chalk.white(l.padEnd(width - 4)) + ' ‚Ēā'));
  
  console.log('\n' + top);
  console.log(titleLine);
  console.log(PINK('‚Ēā' + ' '.repeat(width - 2) + '‚Ēā'));
  contentLines.forEach(l => console.log(l));
  console.log(bottom + '\n');
}
